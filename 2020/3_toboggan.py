from math import prod

def main():
    data = FULL
    lines = data.splitlines()
    grid = lines
    width = len(grid[0])

    def ride(dx, dy):
        x = dx
        y = dy
        trees = 0
        while y < len(grid):
            if grid[y][x % width] == '#':
                trees += 1
            x += dx
            y += dy
        return trees

    print(ride(3, 1))

    checks = [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)]
    answers = [ride(dx, dy) for dx, dy in checks]
    print(answers)
    print(prod(answers))



