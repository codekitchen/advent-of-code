input = DATA
rows = input.each_line.map(&:chomp)
width = rows[0].size

slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2],
]

count_trees = ->(dx, dy) do
    x, y = dx, dy
    trees = 0

    while y < rows.size
        trees += 1 if rows[y][x % width] == '#'
        x += dx
        y += dy
    end
    trees
end

counts = slopes.map { |n| count_trees.(*n) }
p counts
p counts.inject(:*)
